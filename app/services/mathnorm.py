"""Математика из речи в вид, понятный LLM.

Требование куратора: "математические формулы записываются в понятном
для LLM виде". Whisper на русском выдаёт формулу словами:

    "икс в квадрате плюс два икс равно нулю"

Дальше по концепции этот текст читает LLM — генерит конспект, ДЗ, тесты.
Ей нужен однозначный вид: x^2 + 2x = 0.

⚠️ Честно про границы: полное восстановление формул из речи — открытая
задача, правилами она не решается. Здесь покрыты частые школьные
конструкции. Всё, что сложнее, помечается флагом has_math — такие
сегменты можно отдать LLM отдельным проходом уже после ASR.
"""

from __future__ import annotations

import re
from typing import Any

# Порядок важен: длинные конструкции разбираются раньше коротких,
# иначе "в квадрате" съест "квадратный корень".
RULES: list[tuple[str, str]] = [
    # степени
    (r"\bв квадрате\b", "^2"),
    (r"\bв кубе\b", "^3"),
    (r"\bв степени (\w+)\b", r"^\1"),
    # корни
    (r"\bквадратный корень из\b", r"sqrt"),
    (r"\bкорень из\b", r"sqrt"),
    # дроби и операции
    (r"\bделить на\b", "/"),
    (r"\bразделить на\b", "/"),
    (r"\bумножить на\b", "*"),
    # Составные сравнения должны идти раньше их коротких частей.
    (r"\bне равно\b", "!="),
    (r"\bбольше или равно\b", ">="),
    (r"\bменьше или равно\b", "<="),
    (r"\bплюс\b", "+"),
    (r"\bминус\b", "-"),
    (r"\bравно\b", "="),
    (r"\bравняется\b", "="),
    (r"\bбольше\b", ">"),
    (r"\bменьше\b", "<"),
    # греческие
    (r"\bальфа\b", "alpha"),
    (r"\bбета\b", "beta"),
    (r"\bгамма\b", "gamma"),
    (r"\bдельта\b", "delta"),
    (r"\bпи\b", "pi"),
    (r"\bсигма\b", "sigma"),
    (r"\bтета\b", "theta"),
    (r"\bламбда\b", "lambda"),
    # функции
    (r"\bсинус\b", "sin"),
    (r"\bкосинус\b", "cos"),
    (r"\bтангенс\b", "tan"),
    (r"\bлогарифм\b", "log"),
    (r"\bинтеграл\b", "int"),
    (r"\bсумма\b", "sum"),
    (r"\bпроизводная\b", "d/dx"),
    (r"\bбесконечность\b", "inf"),
]

# Числительные в косвенных падежах: "равно нулю", "плюс два".
# Whisper цифры до десяти обычно пишет словами, дальше — цифрами.
NUMERALS: dict[str, str] = {
    "ноль": "0", "нуль": "0", "нуля": "0", "нулю": "0", "нулем": "0",
    "один": "1", "одна": "1", "одного": "1", "единица": "1", "единицы": "1",
    "два": "2", "две": "2", "двух": "2", "двум": "2", "двойка": "2",
    "три": "3", "трех": "3", "трем": "3", "тройка": "3",
    "четыре": "4", "четырех": "4", "четырем": "4",
    "пять": "5", "пяти": "5", "шесть": "6", "шести": "6",
    "семь": "7", "семи": "7", "восемь": "8", "восьми": "8",
    "девять": "9", "девяти": "9", "десять": "10", "десяти": "10",
    "сто": "100", "тысяча": "1000", "тысячи": "1000",
}

# Переменные: одиночная буква как слово. "икс" -> x, но "иксы" не трогаем.
VARIABLES: dict[str, str] = {
    "икс": "x", "игрек": "y", "зет": "z", "эн": "n", "эм": "m",
    "ка": "k", "эй": "a", "би": "b", "цэ": "c",
}

# Признак, что в реплике вообще есть математика — по нему решаем,
# гнать ли сегмент через нормализацию и ставить ли has_math.
MATH_TRIGGERS = re.compile(
    r"\b(в квадрате|в кубе|в степени|корень из|равно|равняется|плюс|минус|"
    r"умножить|разделить|делить на|синус|косинус|тангенс|логарифм|интеграл|"
    r"производная|дискриминант|уравнени|формул|функци)\w*\b",
    re.IGNORECASE,
)


def has_math(text: str) -> bool:
    return bool(MATH_TRIGGERS.search(text.lower()))


def normalize(text: str) -> str:
    """Речевую математику в символьный вид.

    Применять только к сегментам, где has_math() истинно: иначе
    обычная речь пострадает — "больше" и "меньше" встречаются
    в любом разговоре и превратятся в знаки сравнения.
    """
    out = text.lower().replace("ё", "е")

    for word, symbol in VARIABLES.items():
        out = re.sub(rf"\b{word}\b", symbol, out)

    for word, digit in NUMERALS.items():
        out = re.sub(rf"\b{word}\b", digit, out)

    for pattern, replacement in RULES:
        out = re.sub(pattern, replacement, out)

    # sqrt из 16 -> sqrt(16)
    out = re.sub(r"\bsqrt\s+(?:из\s+)?([\w\d^]+)", r"sqrt(\1)", out)
    # "2 x" -> "2x": коэффициент слипается с переменной
    out = re.sub(r"\b(\d+)\s+([a-z])\b", r"\1\2", out)
    # схлопнуть пробелы вокруг операторов: "x ^2 + 2 x" -> "x^2 + 2x"
    out = re.sub(r"\s*\^\s*", "^", out)
    out = re.sub(r"\s{2,}", " ", out)
    return out.strip()


def annotate(text: str) -> tuple[str, bool]:
    """Вернуть (текст, есть_ли_математика).

    Нормализация применяется только при срабатывании триггера —
    это и есть защита от порчи обычной речи.
    """
    if not has_math(text):
        return text, False
    return normalize(text), True


def annotate_timeline(
    timeline: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Добавить LLM-текст и признак математики, сохранив исходный text."""
    result: list[dict[str, Any]] = []

    for utterance in timeline:
        item = dict(utterance)
        llm_text, math_found = annotate(
            str(item.get("text", ""))
        )
        item["llm_text"] = llm_text
        item["has_math"] = math_found
        result.append(item)

    return result
